    /*
     * Faça um programa que receba dois números inteiros e gere os números inteiros
     * que estão no intervalo compreendido por eles.
     */
    public static void main(String[] args) {
        int a = 1;
        int b = 4;
        while (a < b) {
            System.out.println(a);
            a++;
        }

    }
